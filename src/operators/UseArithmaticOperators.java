package operators;

public class UseArithmaticOperators {
	
	//+Additive operator
	//-subtraction
	//*multiplication
	// /division
	//   %Reminder
	public static void main(String[]args) {
		int number1 = 50;
		int number2 = 60;
		
		int summation = number1 + number2;
		System.out.println("Summation value is " + summation);
		
		int subtraction = number1 - number2;
		System.out.println("Subtraction value is " + subtraction);
		
		int multiply = number1 * number2;
		System.out.println("Multiplication value is " + multiply);
		
		double division = number1 / number2;
		System.out.println("Division value is " + division);
		
		
		int reminder = number1 % number2;
		System.out.println("Reminder value is " + reminder);
		
		// 7/2=division value reminder 1
	}
		
	
}
	

