package basic.operators;

public class EqualityAndRelationalOperator {
	
	// ==equal to
	// != not equal
	// > grater than
	// >= grater than equal
	// < less than
	// <=less than equal
	
	public static void main(String[]args) {
		
		int number1 = 39;
		int number2 = 49;
	
		if (number1 == number2) {
			System.out.println("Number1 and Number2 are Equal");
		}
		
		
			if (number1 != number2) {
				System.out.println("Number1 and Number2 are Not Equal");
		}
		
		
			if (number1 > number2) {
				System.out.println("Number1 is Grater than Number2");
			}
		
			if (number1 >= number2) {
				System.out.println("Number1 Grater than Equal Number2");
			}
			
			if (number1  < number2) {
				System.out.println("Number1 is Less than Number2");
			}	
			
			
			if (number1 <= number2) {
				System.out.println("Number1 Less than Equal Number2");
			}
			
	}
	
	
	
}
